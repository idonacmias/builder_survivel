    def draw(self):
        for i, row in enumerate(self.layout):
            for j, cell in enumerate(row):
                point = (i * 100 + self.camra_location[0] ,j * 100 + self.camra_location[1])
                rect = pygame.Rect(point, (100, 100))
                color = 'black'
                if cell: color = 'white'
                pygame.draw.rect(self.game.screen,
                                    color, 
                                    rect)

    def update(self):
        keys = pygame.key.get_pressed()
        self.center_map(keys)
        self.move_map(keys)

    def move_map(self, keys):
        temp_x = (keys[pygame.K_RIGHT] - keys[pygame.K_LEFT]) * self.speed + self.camra_location[0]
        temp_y = (keys[pygame.K_DOWN] - keys[pygame.K_UP]) * self.speed + self.camra_location[1]
        
        self.camra_location[0] = temp_x
        self.camra_location[1] = temp_y

        self.game.player.x += self.speed * (keys[pygame.K_RIGHT] - keys[pygame.K_LEFT]) 
        self.game.player.y += self.speed * (keys[pygame.K_DOWN] - keys[pygame.K_UP]) 


